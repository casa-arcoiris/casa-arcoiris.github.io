			<h1 itemprop="name">El estrés</h1>

			<p>Todas las personas nos hemos sentido o nos sentiremos estresadas, pero ¿Qué podemos hacer para disminuirlo? ¡Aquí te decimos!</p>

			<img itemprop="image" class="post_image" src="https://www.casa-arcoiris.org/assets/estres.jpg" alt="Imagen representando a una persona estresada">
			<p class="post_image-small">Imagen representando a una persona estresada</p>

            <span itemprop="articleBody">
    			<h2>¿Cómo se manifiesta el estrés en el cuerpo?</h2>

    			<p>El estrés se puede manifestar de muchas maneras en el cuerpo. Algunas maneras son:</p>

                <ul class="list_publicacion">
                    <li><p>Caída del cabello</p></li>
                    <li><p>Ansiedad</p></li>
                    <li><p>Pérdida del apetito</p></li>
                    <li><p>Aumento del apetito</p></li>
                    <li><p>Problemas en la piel como acné, dermatitis o psoriasis</p></li>
                    <li><p>Problemas menstruales</p></li>
                    <li><p>Dolores de cabeza</p></li>
                    <li><p>Problemas estomacales (acidez, flatulencias, diarrea, estreñimiento, etc)</p></li>
                    <li><p>Tensiones musculares</p></li>
                    <li><p>Dificultad para respirar</p></li>
                </ul>

                <h2>¿El estrés afecta al crecimiento?</h2>

                <p>Cuando es un estrés  <a onclick="goToSection('estres_agudo')" onkeydown="goToSection('estres_agudo')" class="same_page_link jovenes_link" tabindex="0">agudo</a>, no afecta al crecimiento. Sin embargo, si es un estrés  <a onclick="goToSection('estres_cronico')" onkeydown="goToSection('estres_cronico')" class="same_page_link jovenes_link" tabindex="0">crónico</a> que se ha tenido por un largo plazo de tiempo, sí puede afectarlo.</p>

                <h2 id="estres_agudo">¿Cuál es el estrés agudo?</h2>

                <p>El estrés agudo es el estrés más común. Surge de las exigencias y presiones del pasado reciente y del futuro cercano. Se siente agradable cuando es en pequeñas dosis, pero cuando es demasiado se siente agotador. Por ejemplo, cuando despiertas y sientes una emoción placentera de lo que viene en el día, es estrés en una pequeña dosis, pero cuando regresas de un día muy pesado, sientes agotamiento.</p>

                <h2 id="estres_cronico">¿Cuál es el estrés crónico?</h2>

                <p>A diferencia del estrés agudo, el estrés crónico no es agradable. El estrés crónico es el estrés agotador que desgasta a las personas día a día. El estrés crónico surge cuando una persona nunca ve la salida de una situación deprimente o desgastante. Las personas que tienen estrés crónico olvidan que lo tienen y se acostumbran a tenerlo. El estrés crónico puede causar efectos devastadores en la salud. Los síntomas del estrés crónico son difíciles de tratar y pueden requerir tratamiento médico y psicológico.</p>

                <h2>¿De qué me puedo enfermar si me estreso demasiado?</h2>

                <p>El estrés no causa enfermedades, sin embargo, si se está viviendo con estrés crónico, se pueden bajar las defensas del cuerpo y aumentar el riesgo de atrapar alguna enfermedad.</p>

                <h2>¿Cómo afecta el estrés psicológicamente a largo plazo?</h2>

                <p>El estrés se puede reflejar tanto físicamente como psicológicamente. Algunos síntomas psicológicos del estrés pueden ser:</p>

                <ul class="list_publicacion">
                    <li><p>Fatiga</p></li>
                    <li><p>Cambios de ánimo</p></li>
                    <li><p>Falta de concentración</p></li>
                    <li><p>Insomnio</p></li>
                    <li><p>Irritabilidad</p></li>
                </ul>

                <h2>¿De qué manera puede afectar el estrés en mis relaciones?</h2>

                <p>El estrés puede hacer que te dejes de comunicar de una manera clara, empática y directa, lo cual puede afectar a tus relaciones sociales.</p>

                <p>Es muy importante tener presente que, aunque no la estés pasando bien, tienes que seguir teniendo cuidado con la forma en la que te comunicas con las personas que tienes a tu alrededor.</p>

                <h2>¿El estrés es malo psicológicamente?</h2>

                <p>El estrés, como el resto de nuestras emociones, es adaptable. Aunque no todo lo que sentimos es placentero o agradable, nos sirve como "alerta" sobre nuestra realidad; siempre que una sensación o emoción nos afecta de una manera poco agradable, nos está dando información acerca de situaciones sobre las cuales podemos y debemos actuar.</p>

                <h2>¿De qué manera me puede ayudar el estrés?</h2>

                <p>El estrés nos ayuda a reaccionar ante riesgos y peligros como una "señal de alarma", provoca acciones rápidas cuando necesitamos producir respuestas físicas e intelectuales de emergencia, nos ayuda a generar resistencia gracias a actividades deportivas, entre otros beneficios.</p>

                <h2>¿El estrés tiene alguna relación con el miedo?</h2>

                <p>El estrés y el miedo sí están vinculados, ya que el estrés es una respuesta del cuerpo y mente ante situaciones que de alguna manera nos representan algún peligro, y el miedo aparece cuando creemos que vamos a sufrir daño.</p>

                <p>Te invitamos a leer nuestra publicación <a target="_blank" href="https://www.casa-arcoiris.org/publicaciones/jovenes/emocionario.html" class="same_page_link jovenes_link">Emocionario</a> para obtener más información acerca de las emociones.</p>

                <h2>¿Se puede vivir sin estresarse nunca?</h2>

                <p>No, no se puede. Toda la vida convivimos con emociones que nos son placenteras y con otras que no lo son, es necesario aprender a convivir con estas sensaciones, incluso cuando no nos son agradables.</p>

                <h2>¿Qué actividades ayudan a disminuir el estrés?</h2>

                <p>Aprender a controlar el estrés es un ejercicio constante. Puedes hacer distintas actividades físicas y psicológicas para apoyar los procesos del estrés. Por ejemplo:</p>

                <ul class="list_publicacion">
                    <li><p>Hacer actidades deportivas que sean de agrado personal.</p></li>
                    <li><p>Aprender y practicar ejercicios de respiración.</p></li>
                </ul>

                <h2>¿Qué es recomendable para vivir con el menor estrés posible?</h2>

                <ul class="list_publicacion">
                    <li><p>Hacer actividades que sean de agrado personal</p></li>
                    <li><p>Hacer ejercicio</p></li>
                    <li><p>Dormir temprano</p></li>
                    <li><p>Comer bien</p></li>
                    <li><p>Tener un estilo de vida saludable</p></li>
                    <li><p>Crear un diario donde le puedas dar seguimiento a tus emociones.</p></li>
                    <li><p>Aprender a identificar y mantenerte al tanto de cuáles son las situaciones que te generan estrés.</p></li>
                </ul>

