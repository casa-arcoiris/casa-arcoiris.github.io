			<h1 itemprop="name">La ansiedad</h1>

			<p>¿Alguna vez has experimentado ansiedad? ¿Sabes lo que es? ¿Sabes cómo se siente? ¡Aquí te decimos todo lo que necesitas saber!</p>

			<img itemprop="image" class="post_image" src="https://www.casa-arcoiris.org/assets/ansiedad.jpg" alt="Imagen representando a una persona con ansiedad">
			<p class="post_image-small">Imagen representando a una persona con ansiedad</p>

            <span itemprop="articleBody">
                <h2>¿Qué es la ansiedad?</h2>

                <p>La ansiedad es un sentimiento de miedo, temor e inquietud que se presenta cuando una persona se enfrenta o se va a entrentar a una situación que le parece amenzante.</p>

    			<h2>¿Cómo se manifiesta la ansiedad?</h2>

    			<p>La ansiedad se puede manifestar de muchas maneras. Algunas maneras son:</p>

                <ul class="list_publicacion">
                    <li><p>Ritmo cardiaco más rápido</p></li>
                    <li><p>Respiración más rápida</p></li>
                    <li><p>Sensación de intranquilidad o temor</p></li>
                    <li><p>Sudoración</p></li>
                    <li><p>Problemas estomacales (acidez, flatulencias, diarrea, estreñimiento, etc)</p></li>
                    <li><p>Tensiones musculares</p></li>
                    <li><p>Dolor en el pecho</p></li>
                    <li><p>Dificultad para respirar</p></li>
                    <li><p>Problemas con el sueño (dormir mucho o dormir poco)</p></li>
                    <li><p>Problemas de concentración y pensamientos constantes de la preocupación que se tiene</p></li>
                </ul>

                <h2>¿La ansiedad es una consecuencia del estrés?</h2>

                <p>Hay veces en las que el estrés se puede convertir en ansiedad, sin embargo, no siempre es así.</p>

                <h2>¿Cómo diferenciar entre el estrés y la ansiedad?</h2>

                <p>El estrés y la ansiedad tienen síntomas muy similares, por lo que puede ser difícil diferenciarlos</p>

                <p>Una característica de ambas es la duración, ya que el estrés suele durar poco tiempo, mientras que la ansiedad puede permanecer.</p>

                <p>Te invitamos a leer nuestra publicación <a target="_blank" href="https://www.casa-arcoiris.org/publicaciones/jovenes/el_estres.php" class="same_page_link jovenes_link">El estrés</a> para obtener más información acerca del estrés, sus causas y sus síntomas.</p>

                <h2>¿La ansiedad es mala psicológicamente?</h2>

                <p>Aunque la ansiedad puede resultar poco placentera en cantidades grandes, tiene sus beneficios en dosis pequeñas. Además de activar un estado de alerta, puede ayudar en situaciones en las que no se quiere fallar y le ayuda a las personas a dar lo mejor de sí mismas.</p>

                <h2>¿De qué manera me puede ayudar la ansiedad?</h2>

                <p>La ansiedad puede ayudar a las personas a dar lo mejor de sí, ayuda a tomar mejores decisiones, agudiza los sentidos, entre otras cosas.</p>

                <h2>¿La ansiedad tiene alguna relación con el miedo?</h2>

                <p>La ansiedad y el miedo se pueden sentir de una manera similar. La diferencia es que es producen ante situaciones diferentes.</p>

                <p>El miedo nos hace temer por nuestra seguridad, integridad o incluso nuestra vida. Por otro lado, la ansiedad se puede producir ante una situación que nos parece amenazante para nuestros intereses. Por ejemplo, podemos sentir miedo cuando estamos a punto de ser inyectados o picados con una aguja, mientras que podemos sentir ansiedad cuando estamos a punto de exponer un tema en público.</p>

                <h2>¿Qué actividades ayudan a disminuir la ansiedad?</h2>

                <p>Aprender a controlar la ansiedad es un ejercicio constante. Puedes hacer distintas actividades físicas y psicológicas para apoyar los procesos de la ansiedad. Por ejemplo:</p>

                <ul class="list_publicacion">
                    <li><p>Hacer actidades deportivas que sean de agrado personal.</p></li>
                    <li><p>Aprender y practicar <a onclick="goToSection('ejercicio_de_respiracion')" onkeydown="goToSection('ejercicio_de_respiracion')" class="same_page_link jovenes_link" tabindex="0">ejercicios de respiración</a>.</p></li>
                    <li><p>Hacer <a onclick="goToSection('ejercicio_de_distraccion_mental')" onkeydown="goToSection('ejercicio_de_distraccion_mental')" class="same_page_link jovenes_link" tabindex="0">ejercicios de distracción mental</a>.</p></li>
                </ul>

                <h2 id="ejercicio_de_respiracion">Ejercicio de respiración</h2>

                <p>La respiración profunda es una de las mejores maneras de relajar la mente, reducir el estrés o la ansiedad en el cuerpo, conciliar el sueño y disminuir las emociones intensas en caso de ser molestas en un momento inadecuado.</p>

                <p>Una técnica de respiración conocida para relajarse es la técnica "4-7-8". Esta técnica consiste en inhalar aire por la nariz durante 4 segundos, aguantar el aire durante 7 segundos, exhalar el aire por la boca durante 8 segundos y repetir el proceso 3 veces más o hasta que se sienta menos ansiedad, estrés o se sienta más relajación.</p>

                <h2 id="ejercicio_de_distraccion_mental">Ejercicio de distracción mental</h2>

                <p>La distracción mental puede ser útil para dejar de pensar en un tema que esté afectado de manera negativa a nuestra mente y nos esté generando estrés, ansiedad o algún sentimiendo poco placentero.</p>

                <p>Un ejercicio de distracción es:</p>

                <ul class="list_publicacion">

                    <li><p>Ver 5 cosas del mismo color</p></li>
                    <li><p>Escuchar 4 cosas distintas</p></li>
                    <li><p>Oler 3 cosas distintas</p></li>
                    <li><p>Pensar en 2 cosas distintas que sean de agrado personal</p></li>
                    <li><p>Respirar profundamente</p></li>

                </ul>

                <p>Se pueden cambiar los objetos de este ejercicio, por ejemplo, si no se pueden oler 3 cosas distintas, se puede pensar en 3 animales que sean de agrado personal.</p>

