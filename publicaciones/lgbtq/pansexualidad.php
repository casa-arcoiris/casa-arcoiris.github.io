			<h1 itemprop="name">Pansexualidad</h1>

			<p>Seguramente has escuchado la palabra "pansexual", pero ¿Qué significa? ¿Es lo mismo que la bisexualidad? ¿Es una confusión?</p>

			<img itemprop="image" class="post_image" src="https://www.casa-arcoiris.org/assets/pansexual_flag.webp" alt="Bandera pansexual">
			<p class="post_image-small">Bandera pansexual</p>

			<span itemprop="articleBody">

				<h2>¿Qué es la pansexualidad?</h2>

				<p>La pansexualidad es una orientación sexual. A las personas pansexuales les atraen las cualidades de una persona (su manera de pensar, personalidad, humor, valores, belleza, etc), sin importar su género o su orientación sexual.</p>

				<h2>¿A las personas pansexuales les importa si soy trans?</h2>

				<p>A las personas pansexuales no les interesa si alguien se identifica como trans, cisgénero, no binario, etc.</p>

				<h2>¿La pansexualidad y la bisexualidad son iguales?</h2>

				<p>No. La bisexualidad es la atracción romántica y/o sexual hacia hombres, mujeres, personas transgénero y no binarias.</p>

				<p>La pansexualidad es la atracción romántica y/o sexual sin importar su género o su orientación sexual.</p>

				<p>Te invitamos a leer nuestra publicación <a target="_blank" href="https://www.casa-arcoiris.org/publicaciones/lgbtq/bisexualidad.php" class="same_page_link">Bisexualidad</a> para obtener más información acerca de las personas bisexuales.</p>

				<h2>¿Las personas pansexuales están confundidas?</h2>

				<p>No. Las personas que salen del clóset lo hacen porque la confusión acabó, no porque haya empezado.</p>

				<h2>¿A las personas pansexuales les atrae todo el mundo?</h2>

				<p>No. Las personas pansexuales tienen sus gustos, estándares y preferencias al igual que una persona heterosexual u homosexual.</p>

				<h2>¿Las personas pansexuales tienen sexo todo el tiempo?</h2>

				<p>El sentirse atracción romántica y/o sexual hacia una persona es independiente a tener una vida sexual activa. Las personas pansexuales pueden sentirse atraídas hacia otras personas y no tener sexo con ellas.</p>

				<h2>¿Las personas pansexuales son promiscuas?</h2>

				<p>La pansexualidad y ser una persona promiscua son independientes una de la otra. La orientación sexual no tiene nada que ver con la promiscuidad.</p>

				<h2>¿Las personas pansexuales son infieles?</h2>

				<p>No. La pansexualidad no está de la mano con ser una persona infiel. La infidelidad es independiente de la orientación sexual.</p>

				<h2>¿Ser pansexual es una moda?</h2>

				<p>No. El término ha ganado popularidad porque muchas personas se han identificado con él, pero no es algo nuevo. La pansexualidad siempre ha existido. Las personas que se declaran pansexuales no lo hacen por seguir alguna moda, es por encontrar un término que las represente.</p>

