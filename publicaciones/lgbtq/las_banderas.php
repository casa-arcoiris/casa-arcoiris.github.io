			<h1 itemprop="name">Las banderas</h1>

			<p>¿Alguna vez has visto muchas banderas de colores de la comunidad LGBTQ+ pero no sabes de qué sigla son? ¡Aquí te decimos!</p>

			<img itemprop="image" class="post_image" src="https://www.casa-arcoiris.org/assets/pride-flags.jpg" alt="Banderas representando a la comunidad LGBTQ+">
			<p class="post_image-small">Banderas representando a la comunidad LGBTQ+</p>

			<span itemprop="articleBody">
				<h2>LGBTQ+</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/rainbow_flag.svg" alt="Bandera representando a la comunidad LGBTQ+">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad LGBTQ+</p>

				<p>Esta es la bandera más conocida de la comunidad y es inclusiva para todas las siglas de la comunidad LGBTQ+. La bandera tiene 6 colores: rojo, naranja, amarillo, verde, azul y morado.</p>

				<p>Fue creada por Gilbert Baker en 1978 para la Marcha del Orgullo LGBT+ de San Francisco.</p>

				<p>Te invitamos a leer nuestra publicación <a target="_blank" href="https://www.casa-arcoiris.org/publicaciones/lgbtq/las_siglas.html" class="same_page_link">Las siglas</a> para obtener más información acerca de las siglas de la comunidad LGBTQ+.</p>

				<h2>Lésbica</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/lesbian_flag.webp" alt="Bandera representando a la comunidad de mujeres lesbianas">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad de mujeres lesbianas</p>

				<p>Esta bandera representa a la comunidad de mujeres lesbianas. Han existido muchas versiones de la bandera, sin embargo, esta es la más conocida. La bandera tiene distintas tonalidades de rosa y contiene una franja blanca en el medio.</p>

				<h2>Gay</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/gay_men_flag.jpg" alt="Bandera representando a la comunidad de hombres gays">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad de hombres gays</p>

				<p>Esta bandera representa a la comunidad de hombres gays, hombres trans y género no conforme. La bandera tiene distintas tonalidades de azul y contiene una franja blanca en el medio.</p>

				<p>El autor de la bandera la subió a Tumblr en junio de 2020. Su nombre es desconocido, sin embargo, su nombre de usuario en Tumblr es <a onclick="goToSection('gayflagblog')" onkeydown="goToSection('gayflagblog')" class="same_page_link" tabindex="0">GayFlagBlog</a>.</p>

				<h2>Bisexual</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/bisexual_flag.png" alt="Bandera bisexual">
				<p class="post_image-small banderas_post">Bandera bisexual</p>

				<p>Esta bandera representa a la comunidad de personas bisexuales. La bandera tiene 3 colores: rosa, morado y azul.</p>

				<p>Fue creada por Michael Page en noviembre de 1998.</p>

				<p>Te invitamos a leer nuestra publicación <a target="_blank" href="https://www.casa-arcoiris.org/publicaciones/lgbtq/bisexualidad.php" class="same_page_link">Bisexualidad</a> para obtener más información acerca de las personas bisexuales.</p>

				<h2>Trans</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/trans_flag.jpg" alt="Bandera trans">
				<p class="post_image-small banderas_post">Bandera trans</p>

				<p>Esta bandera representa a la comunidad de personas trans. La bandera tiene 2 franjas de azul pastel, 2 franjas de rosa pastel y una franja blanca en medio.</p>

				<p>Fue creada por Monica Helms en 1999. Su debut fue en Phoenix, Arizona.</p>

				<p>Te invitamos a leer nuestra publicación <a target="_blank" href="https://www.casa-arcoiris.org/publicaciones/lgbtq/transgenero.html" class="same_page_link">¿Transgénero?</a> para obtener más información acerca de las personas trans.</p>

				<h2 id="no_binario">No binario</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/non-binary_flag.webp" alt="Bandera representando a la comunidad de personas no binarias">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad de personas no binarias</p>

				<p>Esta bandera representa a la comunidad de personas no binarias. La bandera tiene 4 colores: amarillo, blanco, morado y negro. La bandera utiliza colores que no están relacionados con ningún género.</p>

				<p>La bandera fue creada por Kye Rowan en 2014.</p>

				<h2>Intersexual</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/intersex_flag.webp" alt="Bandera representando a la comunidad de personas intersexuales">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad de personas intersexuales</p>

				<p>Esta bandera representa a la comunidad de personas intersexuales. La bandera es de color amarillo y tiene un cículo de color morado que simboliza la totalidad. Al igual que la <a onclick="goToSection('no_binario')" onkeydown="goToSection('no_binario')" class="same_page_link" tabindex="0">bandera no binaria</a>, la bandera intersexual contiene colores que no están relacionados con ningún género.</p>

				<p>La bandera fue creada por la Organización Internacional Intersex en Australia en 2013.</p>

				<h2>Asexual</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/asexual_flag.webp" alt="Bandera representando a la comunidad de personas asexuales">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad de personas asexuales</p>

				<p>Esta bandera representa a la comunidad de personas asexuales. La bandera tiene 4 colores: negro, gris, blanco y morado.</p>

				<p>La bandera fue creada en 2010 por un miembro de la <i>Asexual Visibility and Education Network (AVEN)</i> o traducido al español "Red de Educación y Visibilidad Asexual".</p>

				<h2>Arromántica</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/aromantic_flag.jpg" alt="Bandera representando a la comunidad arromántica">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad arromántica</p>

				<p>Esta bandera representa a la comunidad arromántica. La bandera tiene 5 colores: verde pasto, verde limón, blanco, gris y negro.</p>

				<p>La bandera fue propuesta por <i>National Coalition for Aromantic Visibility</i> o traducido al español "Coalición Nacional para la Visibilidad Arromántica".</p>

				<h2>Pansexual</h2>

				<img itemprop="image" class="banderas_post" src="https://www.casa-arcoiris.org/assets/pansexual_flag.webp" alt="Bandera representando a la comunidad de personas pansexuales">
				<p class="post_image-small banderas_post">Bandera representando a la comunidad de personas pansexuales</p>

				<p>Esta bandera representa a la comunidad de personas pansexuales. La bandera tiene 3 colores: magenta, amarillo y cian.</p>

				<p>La bandera empezó a tener visibilidad en 2010.</p>
